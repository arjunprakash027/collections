
import turtle

wn = turtle.Screen()
wn.setup(500,500)

alex = turtle.Turtle()
alex.shape("turtle")
alex.color("red")

while True:
    alex.circle(50)
    alex.forward(100)
    alex.left(90)
    alex.forward(100)
    alex.left(90)
    alex.forward(200)
    alex.left(90)
    alex.forward(100)
    alex.left(90)
    alex.forward(100)
